namespace Spectre.Terminals
{
    /// <summary>
    /// Represents a cursor direction.
    /// </summary>
    public enum CursorDirection
    {
        /// <summary>
        /// Moves the cursor forward.
        /// </summary>
        Forward = 0,

        /// <summary>
        /// Moves the cursor backwards.
        /// </summary>
        Back = 1,

        /// <summary>
        /// Moves the cursor up.
        /// </summary>
        Up = 2,

        /// <summary>
        /// Moves the cursor down.
        /// </summary>
        Down = 3,
    }
}
